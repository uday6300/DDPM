# ATTENTION BLOCK
# =========================

class AttentionBlock(nn.Module):

    def __init__(self, ch):

        super().__init__()

        self.norm = nn.GroupNorm(32, ch)

        self.q = nn.Conv2d(ch, ch, kernel_size=1)
        self.k = nn.Conv2d(ch, ch, kernel_size=1)
        self.v = nn.Conv2d(ch, ch, kernel_size=1)

        self.proj = nn.Conv2d(ch, ch, kernel_size=1)

    def forward(self, x):

        B, C, H, W = x.shape

        h = self.norm(x)

        q = self.q(h).reshape(B, C, H * W)
        k = self.k(h).reshape(B, C, H * W)
        v = self.v(h).reshape(B, C, H * W)

        # attention scores
        attn = torch.einsum(
            "bcn,bcm->bnm",
            q,
            k
        ) / math.sqrt(C)

        attn = torch.softmax(attn, dim=-1)

        # weighted sum
        h = torch.einsum(
            "bnm,bcm->bcn",
            attn,
            v
        )

        h = h.reshape(B, C, H, W)

        h = self.proj(h)

        return x + h


