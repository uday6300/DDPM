class DDPM(nn.Module):

    def __init__(self):

        super().__init__()

        time_dim = base_channels * 4

        # =====================
        # TIME EMBEDDING
        # =====================

        self.time_mlp = nn.Sequential(
            TimeEmbedding(base_channels),

            nn.Linear(base_channels, time_dim),

            nn.ReLU(),

            nn.Linear(time_dim, time_dim)
        )

        # =====================
        # INPUT
        # =====================

        self.conv_in = nn.Conv2d(
            3,
            base_channels,
            kernel_size=3,
            padding=1
        )

        # =====================
        # DOWN
        # =====================

        self.down1 = ResidualBlock(
            base_channels,
            base_channels,
            time_dim
        )

        self.pool1 = nn.AvgPool2d(2)

        self.down2 = ResidualBlock(
            base_channels,
            base_channels * 2,
            time_dim
        )

        self.pool2 = nn.AvgPool2d(2)

        self.down3 = ResidualBlock(
            base_channels * 2,
            base_channels * 4,
            time_dim
        )

        # =====================
        # MIDDLE
        # =====================

        self.mid1 = ResidualBlock(
            base_channels * 4,
            base_channels * 4,
            time_dim
        )

        self.attn = AttentionBlock(
            base_channels * 4
        )

        self.mid2 = ResidualBlock(
            base_channels * 4,
            base_channels * 4,
            time_dim
        )

        # =====================
        # UP
        # =====================

        self.up_s1 = nn.Upsample(
            scale_factor=2,
            mode='nearest'
        )

        self.up1 = ResidualBlock(
            base_channels * 6,     # 256 + 128
            base_channels * 2,     # 128
            time_dim
        )

        # =====================
        # EXTRA ATTENTION
        # =====================

        self.attn_up = AttentionBlock(base_channels * 2)

        self.up_s2 = nn.Upsample(
            scale_factor=2,
            mode='nearest'
        )

        self.up2 = ResidualBlock(
            base_channels * 3,     # 128 + 64
            base_channels,         # 64
            time_dim
        )

        # =====================
        # OUTPUT
        # =====================

        self.conv_out = nn.Conv2d(
            base_channels,
            3,
            kernel_size=3,
            padding=1
        )

    def forward(self, x, t):

        # =====================
        # TIME EMBEDDING
        # =====================

        t = self.time_mlp(t.float())

        # =====================
        # INPUT
        # =====================

        x = self.conv_in(x)

        # =====================
        # DOWN
        # =====================

        d1 = self.down1(x, t)

        h = self.pool1(d1)

        d2 = self.down2(h, t)

        h = self.pool2(d2)

        h = self.down3(h, t)

        # =====================
        # MIDDLE
        # =====================

        h = self.mid1(h, t)

        h = self.attn(h)

        h = self.mid2(h, t)

        # =====================
        # UP STAGE 1
        # =====================

        h = self.up_s1(h)

        h = torch.cat(
            [h, d2],
            dim=1
        )

        h = self.up1(h, t)

        h = self.attn_up(h)

        # =====================
        # UP STAGE 2
        # =====================

        h = self.up_s2(h)

        h = torch.cat(
            [h, d1],
            dim=1
        )

        h = self.up2(h, t)

        # =====================
        # OUTPUT
        # =====================

        h = self.conv_out(h)

        return h



# =========================
# MODEL
# =========================

model = DDPM().to(device)

optimizer = optim.AdamW(
    model.parameters(),
    lr=lr_rate
)


# =========================
# TRAINING LOOP
# =========================

for epoch in range(epochs):

    model.train()

    total_loss = 0

    for x, _ in train_loader:

        x = x.to(device)

        # random timestep
        t = torch.randint(
            0,
            T,
            (x.shape[0],),
            device=device
        )

        # forward diffusion
        xt, noise = forward_diffusion(x, t)

        # predict noise
        noise_pred = model(xt, t)

        # MSE loss
        loss = ((noise_pred - noise) ** 2).mean()

        optimizer.zero_grad()

        loss.backward()

        optimizer.step()

        total_loss += loss.item()

    avg_loss = total_loss / len(train_loader)

    print(f"Epoch {epoch+1}/{epochs} | Loss: {avg_loss:.4f}")

