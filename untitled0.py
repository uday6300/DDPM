@torch.no_grad()
def sample(model, n_samples=16):

    model.eval()

    # start from pure noise
    x = torch.randn(
        n_samples,
        3,
        img_size,
        img_size
    ).to(device)

    for t in reversed(range(T)):

        t_batch = torch.full(
            (n_samples,),
            t,
            device=device,
            dtype=torch.long
        )

        # predict noise
        noise_pred = model(x, t_batch)

        alpha = alphas[t]
        alpha_hat = alpha_bar[t]
        beta = betas[t]

        if t > 0:
            z = torch.randn_like(x)
        else:
            z = torch.zeros_like(x)

        x = (
            1 / torch.sqrt(alpha)
        ) * (
            x -
            ((1 - alpha) /
             torch.sqrt(1 - alpha_hat))
            * noise_pred
        ) + torch.sqrt(beta) * z

    return x

# generate samples
samples = sample(model, 16)

samples = (samples + 1) / 2
samples = samples.clamp(0, 1)

